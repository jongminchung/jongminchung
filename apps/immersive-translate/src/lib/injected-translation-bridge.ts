  function videoIdFromPlayerResponseInPage(playerResponse: unknown): string | null {
    if (!isPageRecord(playerResponse)) return null;
    const videoDetails = playerResponse.videoDetails;
    if (!isPageRecord(videoDetails)) return null;
    return typeof videoDetails.videoId === "string" && videoDetails.videoId.trim()
      ? videoDetails.videoId.trim()
      : null;
  }

  function parseYouTubeTracksInPage(playerResponse: unknown): readonly PageYouTubeTrackCandidate[] {
    if (!isPageRecord(playerResponse) || !isPageRecord(playerResponse.captions)) return [];
    const renderer = playerResponse.captions.playerCaptionsTracklistRenderer;
    if (!isPageRecord(renderer) || !Array.isArray(renderer.captionTracks)) return [];
    const videoId = videoIdFromPlayerResponseInPage(playerResponse);
    return renderer.captionTracks
      .map((track): PageYouTubeTrackCandidate | null => {
        if (!isPageRecord(track) || typeof track.baseUrl !== "string" || !track.baseUrl.trim()) {
          return null;
        }
        const vssId = typeof track.vssId === "string" ? track.vssId : undefined;
        const kind = typeof track.kind === "string" ? track.kind : "";
        return {
          baseUrl: track.baseUrl,
          label: textFromYouTubeName(track.name) ?? "YouTube captions",
          languageCode: typeof track.languageCode === "string" ? track.languageCode : undefined,
          vssId,
          autoGenerated: kind === "asr" || vssId?.startsWith("a.") === true,
          videoId: videoId ?? videoIdFromYouTubeUrlInPage(track.baseUrl),
        };
      })
      .filter((track): track is PageYouTubeTrackCandidate => track !== null);
  }

  function normalizeYouTubeUrlInPage(baseUrl: string): string {
    const url = new URL(baseUrl, location.href);
    url.searchParams.set("fmt", "json3");
    return url.toString();
  }

  function youtubeCaptionUrlCandidatesInPage(baseUrl: string): readonly string[] {
    const formats = ["json3", "srv3", "vtt"] as const;
    return formats.map((format) => {
      const url = new URL(baseUrl, location.href);
      url.searchParams.set("fmt", format);
      return url.toString();
    });
  }

  function normalizedYouTubeLanguageCodeInPage(value: string | undefined): string | null {
    const normalized = value?.trim().toLocaleLowerCase();
    if (!normalized) return null;
    const withoutPrefix = normalized.replace(/^(?:a\.)?\./, "");
    if (withoutPrefix.startsWith("ko") || withoutPrefix.includes("korean")) return "ko";
    if (koreanTextPattern.test(withoutPrefix) && !withoutPrefix.includes("영어")) return "ko";
    if (withoutPrefix.startsWith("en") || withoutPrefix.includes("english")) return "en";
    if (withoutPrefix.includes("영어")) return "en";
    return withoutPrefix.split("-")[0] ?? withoutPrefix;
  }

  function preferredYouTubeLanguageCodesInPage(
    record: Record<string, unknown>,
  ): readonly string[] {
    const selectedLanguageCode =
      typeof record.selectedLanguageCode === "string"
        ? normalizedYouTubeLanguageCodeInPage(record.selectedLanguageCode)
        : null;
    const selectedLabel =
      typeof record.selectedLabel === "string"
        ? normalizedYouTubeLanguageCodeInPage(record.selectedLabel)
        : null;
    if (selectedLanguageCode === "ko" || selectedLabel === "ko") return ["ko", "en"];
    if (selectedLanguageCode === "en" || selectedLabel === "en") return ["en", "ko"];
    const title = typeof record.videoTitle === "string" ? record.videoTitle : document.title;
    if (koreanTextPattern.test(title)) return ["ko", "en"];
    return defaultYouTubeCaptionLanguageCodes;
  }

  function preferredYouTubeLanguageRankInPage(
    track: PageYouTubeTrackCandidate,
    languageCodes: readonly string[],
  ): number {
    const languageCode = normalizedYouTubeLanguageCodeInPage(track.languageCode);
    const rank = languageCodes.findIndex(
      (candidate) => candidate === languageCode,
    );
    return rank >= 0 ? rank : Number.POSITIVE_INFINITY;
  }

  function youtubeTrackPriorityInPage(
    left: PageYouTubeTrackCandidate,
    right: PageYouTubeTrackCandidate,
    languageCodes: readonly string[],
  ): number {
    const languageRank =
      preferredYouTubeLanguageRankInPage(left, languageCodes) -
      preferredYouTubeLanguageRankInPage(right, languageCodes);
    if (languageRank !== 0) return languageRank;
    if (left.autoGenerated !== right.autoGenerated) return left.autoGenerated ? 1 : -1;
    return 0;
  }

  function selectYouTubeTrackInPage(
    tracks: readonly PageYouTubeTrackCandidate[],
    record: Record<string, unknown>,
  ) {
    const selectedBaseUrl =
      typeof record.selectedBaseUrl === "string"
        ? normalizeYouTubeUrlInPage(record.selectedBaseUrl)
        : null;
    if (selectedBaseUrl) {
      const matchingBaseUrl = tracks.find(
        (track) => normalizeYouTubeUrlInPage(track.baseUrl) === selectedBaseUrl,
      );
      if (matchingBaseUrl) return matchingBaseUrl;
    }
    const selectedLabel =
      typeof record.selectedLabel === "string"
        ? record.selectedLabel.trim().toLocaleLowerCase()
        : null;
    if (selectedLabel) {
      const matchingLabel = tracks.find(
        (track) => track.label.trim().toLocaleLowerCase() === selectedLabel,
      );
      if (matchingLabel) return matchingLabel;
    }
    const selectedLanguageCode =
      typeof record.selectedLanguageCode === "string"
        ? normalizedYouTubeLanguageCodeInPage(record.selectedLanguageCode)
        : null;
    if (selectedLanguageCode) {
      const matchingLanguage = tracks.find(
        (track) => normalizedYouTubeLanguageCodeInPage(track.languageCode) === selectedLanguageCode,
      );
      if (matchingLanguage) return matchingLanguage;
    }

    const languageCodes = preferredYouTubeLanguageCodesInPage(record);
    const manualPreferredTrack = tracks.find(
      (track) =>
        !track.autoGenerated &&
        Number.isFinite(preferredYouTubeLanguageRankInPage(track, languageCodes)),
    );
    if (manualPreferredTrack) return manualPreferredTrack;

    const [preferredTrack] = [...tracks]
      .filter((track) => Number.isFinite(preferredYouTubeLanguageRankInPage(track, languageCodes)))
      .sort((left, right) => youtubeTrackPriorityInPage(left, right, languageCodes));
    return preferredTrack ?? tracks.find((track) => !track.autoGenerated) ?? tracks[0] ?? null;
  }

  function parseJsonObjectFromAssignment(source: string, marker: string): unknown | null {
    const markerIndex = source.indexOf(marker);
    if (markerIndex < 0) return null;
    const objectStart = source.indexOf("{", markerIndex + marker.length);
    if (objectStart < 0) return null;

    let depth = 0;
    let inString = false;
    let escaped = false;
    for (let index = objectStart; index < source.length; index += 1) {
      const character = source[index];
      if (inString) {
        if (escaped) {
          escaped = false;
        } else if (character === "\\") {
          escaped = true;
        } else if (character === '"') {
          inString = false;
        }
        continue;
      }

      if (character === '"') {
        inString = true;
      } else if (character === "{") {
        depth += 1;
      } else if (character === "}") {
        depth -= 1;
        if (depth === 0) {
          try {
            return JSON.parse(source.slice(objectStart, index + 1));
          } catch {
            return null;
          }
        }
      }
    }
    return null;
  }

  function readYouTubePlayerResponseFromScriptsInPage(): unknown | null {
    for (const script of Array.from(document.scripts)) {
      const text = script.textContent ?? "";
      if (!text.includes("ytInitialPlayerResponse")) continue;
      const parsed =
        parseJsonObjectFromAssignment(text, "ytInitialPlayerResponse") ??
        parseJsonObjectFromAssignment(text, "window.ytInitialPlayerResponse");
      if (parsed) return parsed;
    }
    return null;
  }

  function parseYouTubePayloadInPage(
    payload: string,
    metadata: {
      readonly label: string;
      readonly languageCode?: string;
      readonly vssId?: string;
      readonly baseUrl: string;
      readonly autoGenerated: boolean;
    },
  ) {
    if (payload.trimStart().startsWith("<")) {
      throw new Error("Only YouTube JSON3 caption payloads are supported.");
    }
    const parsed = JSON.parse(payload);
    if (!isPageRecord(parsed) || !Array.isArray(parsed.events)) {
      throw new Error("YouTube JSON3 caption payload is malformed.");
    }
    const trackId = metadata.vssId?.trim() || metadata.languageCode?.trim() || metadata.baseUrl;
    const language = {
      code: metadata.languageCode,
      label: metadata.label,
      autoGenerated: metadata.autoGenerated,
    };
    const cues = parsed.events
      .map((event) => {
        if (!isPageRecord(event)) return null;
        if (typeof event.tStartMs !== "number") return null;
        if (typeof event.dDurationMs !== "number" || event.dDurationMs <= 0) return null;
        if (!Array.isArray(event.segs)) return null;
        const text = event.segs
          .map((segment) =>
            isPageRecord(segment) && typeof segment.utf8 === "string" ? segment.utf8 : "",
          )
          .join("")
          .trim();
        if (!text) return null;
        return {
          text,
          startTimeSeconds: event.tStartMs / 1000,
          endTimeSeconds: (event.tStartMs + event.dDurationMs) / 1000,
          language,
        };
      })
      .filter((cue) => cue !== null)
      .sort((left, right) => left.startTimeSeconds - right.startTimeSeconds)
      .map((cue, index) => ({
        ...cue,
        id: `${trackId}:${index}`,
        source: { platform: "youtube", trackId, cueId: `${trackId}:${index}` },
      }));
    return {
      id: trackId,
      label: metadata.label,
      language,
      source: { platform: "youtube", trackId },
      cues,
    };
  }

  function decodeCaptionTextInPage(text: string): string {
    const textarea = document.createElement("textarea");
    textarea.innerHTML = text;
    return textarea.value
      .replace(/<[^>]*>/g, "")
      .split("\n")
      .map((line) => line.trim())
      .filter((line) => line.length > 0)
      .join("\n")
      .trim();
  }

  function buildYouTubeTrackInPage(
    drafts: readonly {
      readonly text: string;
      readonly startTimeSeconds: number;
      readonly endTimeSeconds: number;
    }[],
    metadata: {
      readonly label: string;
      readonly languageCode?: string;
      readonly vssId?: string;
      readonly baseUrl: string;
      readonly autoGenerated: boolean;
    },
  ) {
    const trackId = metadata.vssId?.trim() || metadata.languageCode?.trim() || metadata.baseUrl;
    const language = {
      code: metadata.languageCode,
      label: metadata.label,
      autoGenerated: metadata.autoGenerated,
    };
    const cues = drafts
      .filter((cue) => cue.text.trim() && cue.endTimeSeconds > cue.startTimeSeconds)
      .sort((left, right) => left.startTimeSeconds - right.startTimeSeconds)
      .map((cue, index) => ({
        id: `${trackId}:${index}`,
        text: cue.text,
        startTimeSeconds: cue.startTimeSeconds,
        endTimeSeconds: cue.endTimeSeconds,
        language,
        source: {
          platform: "youtube",
          trackId,
          cueId: `${trackId}:${index}`,
        },
      }));
    return {
      id: trackId,
      label: metadata.label,
      language,
      source: { platform: "youtube", trackId },
      cues,
    };
  }

  function parseYouTubeSrv3PayloadInPage(
    payload: string,
    metadata: {
      readonly label: string;
      readonly languageCode?: string;
      readonly vssId?: string;
      readonly baseUrl: string;
      readonly autoGenerated: boolean;
    },
  ) {
    const documentXml = new DOMParser().parseFromString(payload, "text/xml");
    if (documentXml.querySelector("parsererror")) {
      throw new Error("YouTube SRV3 caption payload is malformed.");
    }
    const drafts = Array.from(documentXml.querySelectorAll("p"))
      .map((node) => {
        const start = secondsFromCaptionTimestamp(node.getAttribute("t"));
        const duration = secondsFromCaptionTimestamp(node.getAttribute("d"));
        const text = decodeCaptionTextInPage(node.textContent ?? "");
        if (start === null || duration === null || duration <= 0 || !text) return null;
        return {
          text,
          startTimeSeconds: start / 1000,
          endTimeSeconds: (start + duration) / 1000,
        };
      })
      .filter(
        (
          cue,
        ): cue is {
          readonly text: string;
          readonly startTimeSeconds: number;
          readonly endTimeSeconds: number;
        } => cue !== null,
      );
    return buildYouTubeTrackInPage(drafts, metadata);
  }

  function parseYouTubeVttPayloadInPage(
    payload: string,
    metadata: {
      readonly label: string;
      readonly languageCode?: string;
      readonly vssId?: string;
      readonly baseUrl: string;
      readonly autoGenerated: boolean;
    },
  ) {
    const lines = payload.replace(/^\uFEFF/, "").split(/\r?\n/);
    const drafts: {
      text: string;
      startTimeSeconds: number;
      endTimeSeconds: number;
    }[] = [];
    for (let index = 0; index < lines.length; index += 1) {
      const line = lines[index] ?? "";
      const [rawStart, rawEnd] = line.split("-->");
      if (!rawStart || !rawEnd) continue;
      const start = secondsFromCaptionTimestamp(rawStart.trim());
      const end = secondsFromCaptionTimestamp(rawEnd.trim().split(/\s+/)[0]);
      if (start === null || end === null || end <= start) continue;
      const textLines: string[] = [];
      for (let textIndex = index + 1; textIndex < lines.length; textIndex += 1) {
        const textLine = lines[textIndex] ?? "";
        if (!textLine.trim()) {
          index = textIndex;
          break;
        }
        textLines.push(textLine);
        index = textIndex;
      }
      const text = decodeCaptionTextInPage(textLines.join("\n"));
      if (text) drafts.push({ text, startTimeSeconds: start, endTimeSeconds: end });
    }
    return buildYouTubeTrackInPage(drafts, metadata);
  }

  function parseYouTubeCaptionPayloadCandidateInPage(
    payload: string,
    url: string,
    metadata: {
      readonly label: string;
      readonly languageCode?: string;
      readonly vssId?: string;
      readonly baseUrl: string;
      readonly autoGenerated: boolean;
    },
  ) {
    if (!payload.trim()) throw new Error("YouTube caption payload is empty.");
    const format = new URL(url, location.href).searchParams.get("fmt");
    if (format === "srv3") return parseYouTubeSrv3PayloadInPage(payload, metadata);
    if (format === "vtt") return parseYouTubeVttPayloadInPage(payload, metadata);
    return parseYouTubePayloadInPage(payload, metadata);
  }

  function secondsFromCaptionTimestamp(value: unknown): number | null {
    if (typeof value === "number" && Number.isFinite(value)) return value;
    if (typeof value !== "string") return null;
    const normalized = value.trim().replace(",", ".");
    if (!normalized) return null;
    if (/^\d+(?:\.\d+)?$/.test(normalized)) return Number(normalized);
    const parts = normalized.split(":");
    if (parts.length < 2 || parts.length > 3) return null;
    const numbers = parts.map((part) => Number(part));
    if (numbers.some((number) => !Number.isFinite(number))) return null;
    if (numbers.length === 2) {
      const [minutes, seconds] = numbers;
      return minutes * 60 + seconds;
    }
    const [hours, minutes, seconds] = numbers;
    return hours * 3600 + minutes * 60 + seconds;
  }

  function cleanUdemyCaptionText(text: string): string {
    return text
      .replace(/<[^>]*>/g, "")
      .replace(/&nbsp;/gi, " ")
      .replace(/&amp;/gi, "&")
      .replace(/&lt;/gi, "<")
      .replace(/&gt;/gi, ">")
      .replace(/&quot;/gi, '"')
      .replace(/&#39;/g, "'")
      .split("\n")
      .map((line) => line.trim())
      .filter((line) => line.length > 0)
      .join("\n")
      .trim();
  }

